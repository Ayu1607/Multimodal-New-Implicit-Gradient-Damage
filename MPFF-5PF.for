       DNDZ(2,4)=0.25D0*(1.D0-G)*(2.D0*H-G)
       DNDZ(2,5)=-0.5D0*(1.D0-G*G) 
       DNDZ(2,6)=-(1.D0+G)*H 
       DNDZ(2,7)=0.5D0*(1.D0-G*G)
       DNDZ(2,8)=-(1.D0-G)*H           
          
