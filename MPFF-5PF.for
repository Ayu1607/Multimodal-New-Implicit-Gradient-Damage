       DNDZ(1,4)=0.25D0*(1.D0+H)*(2.D0*G-H)
       DNDZ(1,5)=-G*(1.D0-H)
       DNDZ(1,6)=0.5D0*(1.D0-H*H)
       DNDZ(1,7)=-G*(1.D0+H)
       DNDZ(1,8)=-0.5D0*(1.D0-H*H)      

!     DERIVATIVE D(NI)/D(H)
       DNDZ(2,1)=0.25D0*(1.D0-G)*(G+2.D0*H)
       DNDZ(2,2)=0.25D0*(1.D0+G)*(2.D0*H-G)
