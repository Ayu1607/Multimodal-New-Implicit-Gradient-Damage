       G=COORD34(2,KINTK)
       H=COORD34(3,KINTK)
       
!     SHAPE FUNCTIONS 
       DN(1,1)=(1.D0-F-G-H)*(1.D0-2.D0*F-2.D0*G-2.D0*H)
