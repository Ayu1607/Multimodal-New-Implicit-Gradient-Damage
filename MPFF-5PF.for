       DNDZ(2,4)= 0.125D0*(1.D0-F)*(1.D0-H)
       DNDZ(2,5)=-0.125D0*(1.D0-F)*(1.D0+H)
       DNDZ(2,6)=-0.125D0*(1.D0+F)*(1.D0+H)
       DNDZ(2,7)= 0.125D0*(1.D0+F)*(1.D0+H)
       DNDZ(2,8)= 0.125D0*(1.D0-F)*(1.D0+H)
      
