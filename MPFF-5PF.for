       ELSE ! NEGATIVE OR ZERO JACOBIAN
        WRITE(7,*)'WARNING: ELEMENT',JELEM,'HAS NEG. JACOBIAN'
       ENDIF
       
      ENDIF
      
      DNDX=MATMUL(XJACI,DNDZ)

      RETURN
      END

